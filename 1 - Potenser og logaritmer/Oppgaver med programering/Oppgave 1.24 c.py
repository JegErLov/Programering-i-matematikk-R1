# Lag et program som bestemmer logaritmene
import math
                            # Output:
print(math.log10(7))        # -> 0.8450980400142568
print(math.log10(0.80))     # -> -0.09691001300805639
print(math.log10(4.50))     # -> 0.6532125137753437
print(math.log10(95))       # -> 1.9777236052888478

