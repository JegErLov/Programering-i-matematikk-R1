# Programmet viser verdien av brøkene, ** betyr opphøyd i, / betyr delt på og () brukes for å vise hva som er i brøk

print(49**(1/2))    # 49 opphøyd i en halv
print(3**(8/4))     # 4-rota av 3 oppgøyd i 8 (Vanskelig å forstå)
print(10**(1/2) * 100**(1/3) / 10**(1/6)) # kvadratrota av 10 gange 4-rota av 100 delt på 6-rota av 10